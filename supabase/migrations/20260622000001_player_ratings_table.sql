-- Move player ratings from a hardcoded CSV to a real, editable table.

CREATE TABLE public.player_ratings (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  match_id uuid REFERENCES public.matches(id) ON DELETE SET NULL,
  player_key text NOT NULL,
  player_name text NOT NULL,
  rating numeric(3,1) NOT NULL CHECK (rating >= 0 AND rating <= 10),
  rated_by uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX player_ratings_player_key_idx ON public.player_ratings (player_key);
CREATE INDEX player_ratings_created_at_idx ON public.player_ratings (created_at);
CREATE INDEX player_ratings_match_id_idx ON public.player_ratings (match_id);

ALTER TABLE public.player_ratings ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public read ratings" ON public.player_ratings
  FOR SELECT USING (true);

CREATE POLICY "Non-revoked users can insert ratings" ON public.player_ratings
  FOR INSERT TO authenticated
  WITH CHECK (
    public.has_role(auth.uid(), 'admin') OR NOT public.has_role(auth.uid(), 'user')
  );

CREATE POLICY "Admin or deleter can delete ratings" ON public.player_ratings
  FOR DELETE TO authenticated
  USING (
    public.has_role(auth.uid(), 'admin') OR public.has_role(auth.uid(), 'deleter')
  );

